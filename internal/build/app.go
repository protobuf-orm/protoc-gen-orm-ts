package build

import (
	"fmt"

	"google.golang.org/protobuf/compiler/protogen"
)

type App struct {
	Output string
}

func (a App) NewGeneratedFile(p *protogen.Plugin, frame *Frame, filename string) *protogen.GeneratedFile {
	filepath := fmt.Sprintf("%s/%s", frame.Path, filename)
	gf := p.NewGeneratedFile(filepath, "")
	gf.P(`// Code generated by protoc-gen-orm-ts. DO NOT EDIT.`)
	gf.P(``)

	return gf
}
